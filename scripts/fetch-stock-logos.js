/**
 * Pre-fetch all US stock logos from Massive API
 * Excludes OTC stocks, only fetches from major exchanges (NYSE, NASDAQ, etc.)
 *
 * Usage:
 *   node scripts/fetch-stock-logos.js
 *
 * Rate limit: 5 requests/minute on free tier
 * Estimated time: ~16-20 hours for all US stocks
 */

import fetch from 'node-fetch';
import fs from 'fs/promises';
import fsSync from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

function loadDotEnv(dotenvPath) {
    // Minimal .env loader (no interpolation). Does not override existing env.
    try {
        const raw = fsSync.readFileSync(dotenvPath, 'utf-8');
        for (const line of raw.split('\n')) {
            const trimmed = line.trim();
            if (!trimmed || trimmed.startsWith('#')) continue;
            const idx = trimmed.indexOf('=');
            if (idx === -1) continue;
            const k = trimmed.slice(0, idx).trim();
            let v = trimmed.slice(idx + 1).trim();
            if (!k || process.env[k] !== undefined) continue;
            if (
                (v.startsWith('"') && v.endsWith('"')) ||
                (v.startsWith("'") && v.endsWith("'"))
            ) {
                v = v.slice(1, -1);
            }
            process.env[k] = v;
        }
    } catch {
        // ok if missing
    }
}

loadDotEnv(path.join(__dirname, '..', '.env'));

const MASSIVE_API_KEY = process.env.MASSIVE_API_KEY;
if (!MASSIVE_API_KEY) {
    console.error('❌ MASSIVE_API_KEY not set. Add it to .env (repo root) and re-run.');
    process.exit(1);
}
const MASSIVE_BASE_URL = 'https://api.polygon.io'; // Massive is now Polygon.io

// Files
const OUTPUT_FILE = path.join(__dirname, '..', 'ui', 'public', 'stock-logos.json');
const UNIVERSE_FILE = path.join(__dirname, '..', 'ui', 'public', 'stock-universe.json');
const PROGRESS_FILE = path.join(__dirname, 'fetch-progress.json');

// Rate limit: 5 requests per minute = 12 seconds per request
const DELAY_MS = 12000;

/**
 * Load ticker list from universe file (preferred) or fetch from Polygon API (fallback)
 */
async function fetchAllTickers() {
    // Prefer local universe file (generated by fetch-universe.py)
    try {
        const raw = await fs.readFile(UNIVERSE_FILE, 'utf-8');
        const data = JSON.parse(raw);
        const symbols = data.symbols || {};
        const tickers = Object.entries(symbols).map(([symbol, info]) => ({
            ticker: symbol,
            name: info.name || symbol,
        }));
        console.log(`📋 Loaded ${tickers.length} tickers from universe file`);
        return tickers;
    } catch {
        console.log('⚠️ No universe file found, fetching ticker list from Polygon API...');
    }

    // Fallback: fetch from Polygon API
    const url = `${MASSIVE_BASE_URL}/v3/reference/tickers?market=stocks&active=true&limit=1000&apiKey=${MASSIVE_API_KEY}`;

    let allTickers = [];
    let nextUrl = url;

    while (nextUrl) {
        const response = await fetch(nextUrl);

        if (!response.ok) {
            throw new Error(`API error: ${response.status} ${response.statusText}`);
        }

        const data = await response.json();
        const results = data.results || [];

        // Filter for US stocks only (exclude OTC)
        const usTickers = results.filter(ticker => {
            return ticker.locale === 'us' &&
                   ticker.market === 'stocks' &&
                   ticker.primary_exchange !== 'XOTC'; // Exclude OTC
        });

        allTickers = allTickers.concat(usTickers);

        console.log(`  Found ${usTickers.length} US stocks (total: ${allTickers.length})`);

        // Check for next page
        nextUrl = data.next_url ? `${data.next_url}&apiKey=${MASSIVE_API_KEY}` : null;

        if (nextUrl) {
            await sleep(DELAY_MS);
        }
    }

    console.log(`✅ Total US stocks found: ${allTickers.length}`);
    return allTickers;
}

/**
 * Fetch ticker details including logo
 */
async function fetchTickerDetails(ticker) {
    const url = `${MASSIVE_BASE_URL}/v3/reference/tickers/${ticker}?apiKey=${MASSIVE_API_KEY}`;

    try {
        const response = await fetch(url);

        if (!response.ok) {
            console.error(`  ❌ Error fetching ${ticker}: ${response.status}`);
            return null;
        }

        const data = await response.json();
        const results = data.results;

        if (!results || !results.branding) {
            return null;
        }

        return {
            ticker: results.ticker,
            name: results.name,
            iconUrl: results.branding.icon_url
                ? `${results.branding.icon_url}?apiKey=${MASSIVE_API_KEY}`
                : null,
            logoUrl: results.branding.logo_url
                ? `${results.branding.logo_url}?apiKey=${MASSIVE_API_KEY}`
                : null,
        };
    } catch (error) {
        console.error(`  ❌ Exception fetching ${ticker}:`, error.message);
        return null;
    }
}

/**
 * Load progress from file
 */
async function loadProgress() {
    try {
        const data = await fs.readFile(PROGRESS_FILE, 'utf-8');
        return JSON.parse(data);
    } catch {
        return { processed: [], logos: {} };
    }
}

/**
 * Save progress to file
 */
async function saveProgress(progress) {
    await fs.writeFile(PROGRESS_FILE, JSON.stringify(progress, null, 2));
}

/**
 * Save final logo data
 */
async function saveLogos(logos) {
    // Ensure public directory exists
    const publicDir = path.join(__dirname, '..', 'ui', 'public');
    await fs.mkdir(publicDir, { recursive: true });

    await fs.writeFile(OUTPUT_FILE, JSON.stringify(logos, null, 2));
    console.log(`\n✅ Saved ${Object.keys(logos).length} logos to ${OUTPUT_FILE}`);
}

/**
 * Sleep utility
 */
function sleep(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
}

/**
 * Main execution
 */
async function main() {
    console.log('🚀 Starting US stock logo fetch...\n');

    // Load previous progress
    const progress = await loadProgress();
    const processedSet = new Set(progress.processed || []);
    const logos = progress.logos || {};

    // Fetch all tickers
    const tickers = await fetchAllTickers();
    const tickersToFetch = tickers.filter(t => !processedSet.has(t.ticker));

    console.log(`\n📊 Progress: ${processedSet.size}/${tickers.length} already processed`);
    console.log(`⏳ Remaining: ${tickersToFetch.length} tickers\n`);

    if (tickersToFetch.length === 0) {
        console.log('✅ All tickers already processed!');
        await saveLogos(logos);
        return;
    }

    const estimatedMinutes = Math.ceil((tickersToFetch.length * DELAY_MS) / 60000);
    console.log(`⏱️  Estimated time: ~${estimatedMinutes} minutes (${(estimatedMinutes / 60).toFixed(1)} hours)\n`);

    // Process each ticker
    for (let i = 0; i < tickersToFetch.length; i++) {
        const ticker = tickersToFetch[i];
        const symbol = ticker.ticker;

        console.log(`[${i + 1}/${tickersToFetch.length}] Fetching ${symbol}...`);

        const details = await fetchTickerDetails(symbol);

        if (details && (details.iconUrl || details.logoUrl)) {
            logos[symbol] = {
                name: details.name,
                iconUrl: details.iconUrl,
                logoUrl: details.logoUrl,
            };
            console.log(`  ✓ Got logo for ${symbol}`);
        } else {
            console.log(`  - No logo for ${symbol}`);
        }

        // Mark as processed
        processedSet.add(symbol);
        progress.processed = Array.from(processedSet);
        progress.logos = logos;

        // Save progress every 10 tickers
        if ((i + 1) % 10 === 0) {
            await saveProgress(progress);
            console.log(`  💾 Progress saved (${Object.keys(logos).length} logos collected)`);
        }

        // Respect rate limit (except for last item)
        if (i < tickersToFetch.length - 1) {
            await sleep(DELAY_MS);
        }
    }

    // Save final results
    await saveProgress(progress);
    await saveLogos(logos);

    console.log('\n🎉 Done!');
    console.log(`📊 Total logos collected: ${Object.keys(logos).length}/${tickers.length}`);
}

// Run
main().catch(error => {
    console.error('❌ Fatal error:', error);
    process.exit(1);
});
